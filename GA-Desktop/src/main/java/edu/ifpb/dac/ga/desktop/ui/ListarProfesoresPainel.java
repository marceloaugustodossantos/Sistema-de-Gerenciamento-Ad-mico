    private void listarProfessoresNaTabela() {

        ProfessorService professorService = ServicesFacade.getProfessorService();
        List<Professor> professores = professorService.listarTodosProfessores();
        DefaultTableModel valor = (DefaultTableModel) tabela.getModel();
        for (Professor professor : professores) {
            valor.addRow(new Object[]{professor});
        }
    }

