    private void listarAdministradores() {
        CursoService cursoService = ServicesFacade.getCursoService();
        List<Curso> cursos = cursoService.listarTodosOsCursos();
        DefaultTableModel valor = (DefaultTableModel) tabela.getModel();
        for (Curso curso : cursos) {
            valor.addRow(new Object[]{curso});
        }
    }

