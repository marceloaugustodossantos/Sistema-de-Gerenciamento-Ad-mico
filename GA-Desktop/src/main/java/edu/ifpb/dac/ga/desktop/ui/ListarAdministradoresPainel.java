    private void listarAdministradores() {

        AdministradorService administradorService = ServicesFacade.getAdministradorService();
        List<Administrador> administradores = administradorService.listarAdministradores();
        DefaultTableModel valor = (DefaultTableModel) tabelaAdministradores.getModel();
        for (Administrador administrador : administradores) {
            valor.addRow(new Object[]{administrador});
        }
    }

