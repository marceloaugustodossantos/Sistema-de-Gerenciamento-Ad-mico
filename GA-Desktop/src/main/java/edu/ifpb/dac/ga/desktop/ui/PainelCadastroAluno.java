    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Aluno aluno = new Aluno();
        aluno.setNome(campoNome.getText());
        aluno.setCpf(campoCPF.getText());
        aluno.setRg(campoRG.getText());
        aluno.setEmail(campoEmail.getText());
        aluno.setMatricula(campoMatricula.getText());
        aluno.setSenha(campoMatricula.getText());
        
        aluno.setCurso((Curso) cursoComboBox.getSelectedItem());
        Validador<Aluno> validador = new Validador();
        if (validador.ehValido(aluno)) {
            AlunoService alunoService = ServicesFacade.getAlunoService();
            alunoService.cadastrar(aluno);
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
            campoCPF.setText("");
            campoEmail.setText("");
            campoMatricula.setText("");
            campoNome.setText("");
            campoRG.setText("");
        }else{
            JOptionPane.showMessageDialog(null, validador.getErros());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

