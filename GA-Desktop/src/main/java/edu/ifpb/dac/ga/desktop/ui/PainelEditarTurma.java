    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        turma.setDisciplina((Disciplina) comboBoxDisciplinas.getSelectedItem());
        turma.setProfessor((Professor) comboBoxProfessores.getSelectedItem());
        turma.setIdentificacao(campoIdentificacao.getText());
        //    turma.setAlunos(this.alunos);
        Validador<Turma> validador = new Validador();
        if (validador.ehValido(turma)) {
            TurmaService turmaService = ServicesFacade.getTurmaService();
            turmaService.atualizar(turma);
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
            campoIdentificacao.setText("");
            DefaultTableModel valor = (DefaultTableModel) tabelaAlunos.getModel();
            this.framePrincipal.setContentPane(new PainelEditarTurma(this.framePrincipal, this.turma));
            this.framePrincipal.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, validador.getErros());
        }
    }//GEN-LAST:event_btSalvarActionPerformed

