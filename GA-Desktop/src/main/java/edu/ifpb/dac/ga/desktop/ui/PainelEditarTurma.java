    private void listarAlunosNaTabela() {
        DefaultTableModel valor = (DefaultTableModel) tabelaAlunos.getModel();
        for (Aluno aluno : alunosTurma) {
            valor.addRow(new Object[]{aluno.getNome(), aluno.getMatricula()});
        }
    }

    private void carregarComboBoxCurso() {
        CursoService cursoService = ServicesFacade.getCursoService();
        cursos = cursoService.listarTodosOsCursos();
        for (Curso curso : cursos) {
            comboBoxCursos.addItem(curso);
        }
    }

    private void carregarComboBoxProfessores() {
        ProfessorService professorService = ServicesFacade.getProfessorService();
        professores = professorService.listarTodosProfessores();
        for (Professor professor : professores) {
            comboBoxProfessores.addItem(professor);
        }
    }

    private void carregarComboBoxAlunos() {
        AlunoService alunoService = ServicesFacade.getAlunoService();
        alunosTodos = alunoService.listarTodosAlunos();
        for (Aluno aluno : alunosTodos) {
            comboBoxAlunos.addItem(aluno);
        }
    }

    private void carregarComboBoxDisciplinas() {
        DisciplinaService disciplinaService = ServicesFacade.getDisciplinaService();
        disciplinas = disciplinaService.listarTodasDisciplinas();
        for (Disciplina disciplina : disciplinas) {
            comboBoxDisciplinas.addItem(disciplina);
        }
    }

