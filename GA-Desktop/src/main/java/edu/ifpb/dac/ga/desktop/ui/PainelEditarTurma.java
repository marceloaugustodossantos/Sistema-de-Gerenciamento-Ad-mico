    private void btAddAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddAlunoActionPerformed
        Aluno aluno = (Aluno) comboBoxAlunos.getSelectedItem();
        if (!alunosTurma.contains(aluno)) {
            alunosTurma.add(aluno);
            DefaultTableModel valor = (DefaultTableModel) tabelaAlunos.getModel();
            valor.addRow(new Object[]{aluno.getNome(), aluno.getMatricula()});
        } else {
            JOptionPane.showMessageDialog(null, "Aluno já cadastrado na turma!");
        }

    }//GEN-LAST:event_btAddAlunoActionPerformed


