    private void listarDisciplinas() {
        DisciplinaService disciplinaService = ServicesFacade.getDisciplinaService();
        List<Disciplina> disciplinas = disciplinaService.listarTodasDisciplinas();
        DefaultTableModel valor = (DefaultTableModel) tabela.getModel();
        for (Disciplina disciplina : disciplinas) {
            valor.addRow(new Object[]{disciplina});
        }
    }

